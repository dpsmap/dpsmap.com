                <h3>What to see ?</h3>
                <div class="description">
                <p>
                    <b>Ananda Temple</b> --- The Ananda Temple built after the Shwe Zigon in 1090 is the masterpiece of the early style temple architecture. There are four huge Buddha images in the standing position and eighty relieves depicting the life of the Buddha from his Birth to his Enlightenment. Ananda Pagoda Festival is yearly celebrated on Full moon day of Pyartho.
                    </br></br>
                    <b>Shwe Zigon Pagoda</b> --- This golden pagoda was the first monument built in the Myanmar style, the prototype for later pagodas. It was first built by King Anawrahta and completed by King Kyan Sittha in 1087. 'Nat' (spirit) images can be found within its precincts. Shwe Zigon Festival is celebrated on full moon day of Tasungmone.
                    </br></br>
                    <b>Thatbyinnyu Temple</b> --- Over 66 meters high, and built by King Alaungsithu in the middle of the 12th century, this white stucco building overtops all other monuments as the highest pagoda on the Bagan plain.
                    </br></br>
                    <b>Manuhar Pagoda</b> --- The Manuhar Payar was built by King Manuhar from Thaton. King Manuhar sold a piece of his jewelry for a fabulous sum in Bagan and built a shine there, probably to portray his life as a POW, (Prisoner-of-war). You can see the shrine today huge images of the Buddha inside a low and narrow building, clearly conveying a sense of confinement. Manuhar Pagoda festival is yearly celebrate on First day over Thidingyut full-moon day.
                    </br></br>
                    <b>Dhamma Yangyi Temple</b> --- Bagan's most massive temple, Dhamma Yangyi Temple was built by King Narathu in 1167. This temple was not finished but it displayed the finest brickwork.
                    </br></br>
                    <b>Shwegugyi Temple</b> --- Standing on the high brick plinth, this temple was built by King Alaungsithu in 1131 AD. The arch pediments, pilasters, plinth and cornice molding are decorated with fine stucco carvings, evident of Myanmar architecture of the early 12th Century.
                    </br></br>
                    <b>Gawdaw Palin Temple</b> --- This 13th century temple is like That Byinnyu, about 60 metres high with a fine view of the Bagan plains. Festival of Gawdaw Palin Temple is celebrated on full moon day of Thidingyut.
                    </br></br>
                    <b>Gubyaukgyi Temple (Wetkyi-In)</b> --- It is a 13th century temple with a spire resembling the Mahabodhi Temple at Bodh Gaya in India. This temple is known for its wall paintings depicting scenes from the Jatakas (life stories of the Buddha).
                    </br></br>
                    <b>Bupaya Pagoda</b> --- Standing on the brink of the Ayeyarwaddy River, the Bupaya Pagoda is a conspicuous landmark for travelers along the river. This pagoda with bulbous dome resembling the "Bu" or gourd is a favorite spot for visitors to watch the sunset.
                    </br></br>
                    <b>Archaeological Museum</b> --- The museum run by Archaeological Department is situated near the Gawdawpalin Temple. It has a collection of more than 2,000 items including Buddha statues, stucco pieces, terra-cotta cups and pots. Open daily except Monday and public holidays.
                    </br></br>
                    <b>Shopping</b> --- Masterpieces of lacquerware have been the pride of Bagan since the days of the Bagan Empire. It is still the main industry of Bagan today and you can observe the making process of lacquerware from the beginning to the finished products ready for sale at the shops. Lacquerware like bowls, boxes, trays and paintings are the best souvenirs of Bagan.
                    </br></br>
